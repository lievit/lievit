package dev.lievit.kit;

/**
 * Where an icon sits relative to its label (the Filament {@code IconPosition}). Used by a stat's
 * description icon and by action buttons.
 */
public enum IconPosition {
    /** The icon precedes the label. */
    BEFORE,
    /** The icon follows the label. */
    AFTER
}
